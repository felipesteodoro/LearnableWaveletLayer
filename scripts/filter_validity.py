fig, ax = plt.subplots(figsize=(6.5, 3.5))
ax.stem(np.arange(L), h_db4, linefmt="C0-", markerfmt="C0o", basefmt=" ", label="Db4 (ref.)")
ax.stem(np.arange(L) + 0.1, h_lwt, linefmt="C3-", markerfmt="C3s", basefmt=" ", label="LWT-Warm")
ax.set_xlabel("n"); ax.set_ylabel("h[n]"); ax.legend(); ax.grid(True, alpha=0.3)
ax.set_title("Filtro passa-baixa (domínio do tempo)")
plt.tight_layout(); plt.savefig(OUT_FIG / "fig_learned_filter_time.pdf"); plt.close()

# Frequency response
w = np.linspace(0, np.pi, 512)
Hdb4 = np.abs(np.fft.fft(h_db4, 1024))[:512]
Hlwt = np.abs(np.fft.fft(h_lwt, 1024))[:512]
Gdb4 = np.abs(np.fft.fft(g_db4, 1024))[:512]
Glwt = np.abs(np.fft.fft(g_lwt, 1024))[:512]
fig, axes = plt.subplots(1, 2, figsize=(10, 3.5))
axes[0].plot(w, Hdb4, label="H Db4", color="C0")
axes[0].plot(w, Hlwt, label="H LWT-Warm", color="C3", ls="--")
axes[0].set_title("Passa-baixa |H(ω)|"); axes[0].grid(True, alpha=0.3); axes[0].legend()
axes[0].set_xlabel("ω"); axes[0].set_xlim(0, np.pi)
axes[1].plot(w, Gdb4, label="G Db4", color="C0")
axes[1].plot(w, Glwt, label="G LWT-Warm", color="C3", ls="--")
axes[1].set_title("Passa-alta |G(ω)|"); axes[1].grid(True, alpha=0.3); axes[1].legend()
axes[1].set_xlabel("ω"); axes[1].set_xlim(0, np.pi)
plt.tight_layout(); plt.savefig(OUT_FIG / "fig_learned_filter_freq.pdf"); plt.close()

# Correlation with classical wavelets
fig, ax = plt.subplots(figsize=(5.5, 3.5))
names = list(corrs.keys()); vals = list(corrs.values())
ax.bar(names, vals, color=["C0", "C1", "C2", "C3"])
ax.set_ylim(0, 1.05); ax.set_ylabel("Correlação")
ax.axhline(0.95, color="r", ls="--", lw=0.8, label="0.95")
ax.set_title("Similaridade com wavelets clássicas"); ax.legend(); ax.grid(True, alpha=0.3)
plt.tight_layout(); plt.savefig(OUT_FIG / "fig_learned_filter_vs_classic.pdf"); plt.close()

